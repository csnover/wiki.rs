#[track_caller]
fn test_projection(projection: ProjectionSettings, tests: &[([f64; 2], [f64; 2])]) {
    test_projection_epsilon(projection, tests, 1e-5);
}

#[track_caller]
fn test_projection_epsilon(
    projection: ProjectionSettings,
    tests: &[([f64; 2], [f64; 2])],
    epsilon: f64,
) {
    let settings = ProjectorSettings {
        projection,
        ..Default::default()
    };
    let projector = Projector::new(&settings);
    test_projector_epsilon(&projector, tests, epsilon);
}

#[track_caller]
fn test_projector(projector: &Projector, tests: &[([f64; 2], [f64; 2])]) {
    test_projector_epsilon(projector, tests, 1e-5);
}

#[track_caller]
fn test_projector_epsilon(projector: &Projector, tests: &[([f64; 2], [f64; 2])], epsilon: f64) {
    for (test, expected) in tests {
        assert_approx_epsilon(
            projector.projection_degrees(Vec2::new(test[0], test[1])),
            Vec2::new(expected[0], expected[1]),
            epsilon,
        );
    }
}
