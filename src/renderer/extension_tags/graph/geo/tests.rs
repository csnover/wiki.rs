#[test]
fn rotation() {
    #[track_caller]
    fn rotate(rotator: &Rotator, [tx, ty]: [f64; 2], [ex, ey]: [f64; 2]) {
        let test = Vec2::new(tx, ty).to_radians();
        assert_approx_epsilon(rotator.rotate(test).to_degrees(), Vec2::new(ex, ey), 1e-6);
    }

    let rotator = Rotator::from(Rotate::TwoD([90.0, 0.0]));
    rotate(&rotator, [0.0, 0.0], [90.0, 0.0]);
    rotate(&rotator, [150.0, 0.0], [-120.0, 0.0]);
    let rotator = Rotator::from(Rotate::TwoD([-45.0, 45.0]));
    rotate(&rotator, [0.0, 0.0], [-54.73561, 30.0]);
    assert_approx_epsilon(
        rotator
            .invert(Vec2::new(-54.73561, 30.0).to_radians())
            .to_degrees(),
        Vec2::new(0.0, 0.0),
        1e-6,
    );
}

